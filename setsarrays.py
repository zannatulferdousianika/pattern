set1 = {'apple','orange','banana'}
set2 = {'mango','berry','apple'}

print("set 1:",set1)            
print("set 2:",set2)     

print("set union:",set1.union(set2))            
print("set intersection:",set1.intersection(set2))            
print("set 1-2 :",set1.difference(set2))            
print("set 2-1:",set2.difference(set1))  
print("set 2-1:",set1.symmetric_difference(set2))            



